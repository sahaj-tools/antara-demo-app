/**
 * Why this exists:
 * Some hosts + Next.js static export (Turbopack) do not reliably replace `process.env.NEXT_PUBLIC_*`
 * in client bundles, so runtime reads are undefined in the browser even when CI env is set.
 *
 * What we do:
 * Before `next dev` / `next build`, read the same variables from `process.env` and write
 * `lib/generated-public-env.ts` with plain string literals. The bundler then cannot strip them.
 *
 * Alternatives:
 * - `public/config.json` fetched at runtime (extra request, must not cache stale config).
 * - Server-side only OAuth (no public env in the browser).
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { config } from "dotenv";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

config({ path: path.join(root, ".env.local"), quiet: true });
config({ path: path.join(root, ".env"), quiet: true });

const KEYS = [
  "NEXT_PUBLIC_API_BASE",
  "NEXT_PUBLIC_APP_ID",
  "NEXT_PUBLIC_REDIRECT_URI",
];

const values = {};
for (const key of KEYS) {
  const raw = process.env[key];
  if (raw === undefined || String(raw).trim() === "") {
    console.error(
      `[inject-public-env] Missing ${key}. Set it in the environment (e.g. Cloudflare Pages variables) or in .env.local for local dev.`,
    );
    process.exit(1);
  }
  values[key] = String(raw).trim();
}

const outPath = path.join(root, "lib", "generated-public-env.ts");
const contents = `/* AUTO-GENERATED by scripts/inject-public-env.mjs — do not edit */
export const INLINED_PUBLIC_ENV = {
  NEXT_PUBLIC_API_BASE: ${JSON.stringify(values.NEXT_PUBLIC_API_BASE)},
  NEXT_PUBLIC_APP_ID: ${JSON.stringify(values.NEXT_PUBLIC_APP_ID)},
  NEXT_PUBLIC_REDIRECT_URI: ${JSON.stringify(values.NEXT_PUBLIC_REDIRECT_URI)},
} as const;
`;

fs.writeFileSync(outPath, contents, "utf8");
console.log(`[inject-public-env] Wrote ${path.relative(root, outPath)}`);
